package com.example.mydairyapp.object;

public class RecyclerContent {
    public RecyclerContent(String idx, String content, String date, String title) {
        this.idx = idx;
        this.content = content;
        this.date = date;
        this.title = title;
    }

    public String idx;
    public String content;
    public String date;
    public String title;
}